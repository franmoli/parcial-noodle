package ar.edu.utn.frba.dds;

public class Alumno {
  String nombre;
  String apellido;
  String mail;
  String username;

  public String getMail(){
    return mail;
  }

  public String getNombre(){
    return nombre;
  }

  public String getApellido(){
    return apellido;
  }

  public String getUsername(){
    return username;
  }
}
